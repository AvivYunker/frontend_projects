// array - a collection of related data
// how to create an array?
let arr = [1,2,3,4];

// how to retreive array elements?
console.log(arr[0]); // 1
console.log(arr[3]); // 4

// How to know if an array includes an element?
console.log(arr.includes(1)); // true
console.log(arr.includes(8)); // false

// How to add elements to the end of an array?
arr.push(5);

// How to remove elements from the end of an array?
arr.pop();


